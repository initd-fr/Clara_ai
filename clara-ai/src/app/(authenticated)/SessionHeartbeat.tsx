"use client";

////////////////////////////////////////////////////////////////////////////////IMPORTS///////////////////////////////////////////////////////////////////////////////////////
import { useEffect, memo, useCallback, useState } from "react";
import { api } from "~/trpc/react";
import { signOut } from "next-auth/react";
import { toast } from "react-hot-toast";
////////////////////////////////////////////////////////////////////////////////IMPORTS///////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////TYPES///////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////TYPES///////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////FUNCTIONS///////////////////////////////////////////////////////////////////////////////////////
const SessionHeartbeat = memo(function SessionHeartbeat() {
  const { error } = api.auth.checkSession.useQuery(undefined, {
    refetchInterval: 10000, // toutes les 10 secondes
    refetchOnWindowFocus: true,
    retry: false,
  });
  const [isLoggingOut, setIsLoggingOut] = useState(false);

  ////////////////////////////////////////////////////////////////////////////////HOOKS///////////////////////////////////////////////////////////////////////////////////////
  const handleSessionError = useCallback(async () => {
    // Éviter les redirections multiples
    if (isLoggingOut) {
      console.log("🔒 Force logout déjà en cours, ignoré");
      return;
    }

    console.log("🚨 FORCE LOGOUT DÉTECTÉ - SessionHeartbeat");
    setIsLoggingOut(true);

    try {
      toast.error("Session expirée ou déconnectée ailleurs.");
      await signOut({ redirect: false });
      console.log("🔄 Redirection vers /auth...");
      // Utiliser window.location.href pour une redirection plus fiable
      window.location.href = "/auth";
    } catch (error) {
      console.error("Erreur lors de la déconnexion:", error);
      // Fallback : redirection directe
      try {
        window.location.href = "/auth";
      } catch (redirectError) {
        console.error("Erreur lors de la redirection:", redirectError);
        window.location.reload();
      }
    }
  }, [isLoggingOut]);

  useEffect(() => {
    if (error) {
      // Ignorer les erreurs de stream fermé
      if (error.message.includes("Controller is already closed")) {
        return;
      }

      // Gérer les erreurs de session
      if (
        error.message === "FORCE_LOGOUT" ||
        error.message.includes("Session")
      ) {
        handleSessionError();
      }
    }
  }, [error, handleSessionError]);
  ////////////////////////////////////////////////////////////////////////////////HOOKS///////////////////////////////////////////////////////////////////////////////////////

  ///////////////////////////////////////////////////////////////////////////////RENDER///////////////////////////////////////////////////////////////////////////////////////
  return null;
});
////////////////////////////////////////////////////////////////////////////////FUNCTIONS///////////////////////////////////////////////////////////////////////////////////////

export default SessionHeartbeat;
