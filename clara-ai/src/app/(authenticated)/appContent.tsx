"use client";

////////////////////////////////////////////////////////////////////////////////IMPORTS///////////////////////////////////////////////////////////////////////////////////////
import { useSession, signOut } from "next-auth/react";
import { useEffect, memo, useCallback } from "react";
import toast from "react-hot-toast";
import { api } from "~/trpc/react";
import Sidebar from "~/app/(authenticated)/sidebar/index";
////////////////////////////////////////////////////////////////////////////////IMPORTS///////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////FUNCTIONS///////////////////////////////////////////////////////////////////////////////////////
// Hook personnalisé pour la vérification de session
function useSessionCheck() {
  const query = api.auth.checkSession.useQuery(undefined, {
    retry: false,
    refetchInterval: 30000,
    refetchOnWindowFocus: true,
  });

  const handleSignOut = useCallback(() => {
    void signOut({
      redirect: false,
    });
    try {
      window.location.href = "/auth";
    } catch (error) {
      console.error("Erreur lors de la redirection:", error);
      // Fallback : recharger la page
      window.location.reload();
    }
  }, []);

  useEffect(() => {
    if (query.error) {
      // Ignorer les erreurs de stream fermé
      if (query.error.message.includes("Controller is already closed")) {
        return;
      }

      if (query.error.message === "FORCE_LOGOUT") {
        toast.error("Nouvelle session détectée, déconnexion...");
        handleSignOut();
      } else {
        toast.error("Session invalide, déconnexion...");
        handleSignOut();
      }
    }
  }, [query.error, handleSignOut]);

  return query;
}

const AppContent = memo(function AppContent() {
  ////////////////////////////////////////////////////////////////////////////////STATE///////////////////////////////////////////////////////////////////////////////////////
  const { status } = useSession();

  useSessionCheck();

  ////////////////////////////////////////////////////////////////////////////////HOOKS///////////////////////////////////////////////////////////////////////////////////////
  useEffect(() => {
    if (status === "unauthenticated") {
      toast.error("Non authentifié, redirection...");
      try {
        window.location.href = "/auth";
      } catch (error) {
        console.error("Erreur lors de la redirection:", error);
        window.location.reload();
      }
    }
  }, [status]);
  ////////////////////////////////////////////////////////////////////////////////HOOKS///////////////////////////////////////////////////////////////////////////////////////

  if (status === "loading") {
    return null;
  }

  ///////////////////////////////////////////////////////////////////////////////RENDER///////////////////////////////////////////////////////////////////////////////////////
  return <Sidebar />;
});
////////////////////////////////////////////////////////////////////////////////FUNCTIONS///////////////////////////////////////////////////////////////////////////////////////

export default AppContent;
