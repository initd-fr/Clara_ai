////////////////////////////////////////////////////////////////////////////////IMPORTS///////////////////////////////////////////////////////////////////////////////////////
import React, { useEffect, useCallback } from "react";
////////////////////////////////////////////////////////////////////////////////IMPORTS///////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////TYPES///////////////////////////////////////////////////////////////////////////////////////
// Types pour les événements
type MouseEvent = globalThis.MouseEvent;
type TouchEvent = globalThis.TouchEvent;
type EventTarget = MouseEvent | TouchEvent;

// Type pour le callback
type OutsideClickCallback = (event: EventTarget) => void;

// Type générique pour la ref
type ElementRef<T extends HTMLElement = HTMLElement> = React.RefObject<T>;
////////////////////////////////////////////////////////////////////////////////TYPES///////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////FUNCTIONS///////////////////////////////////////////////////////////////////////////////////////
export const useOutsideClick = <T extends HTMLElement = HTMLElement>(
  ref: ElementRef<T>,
  callback: OutsideClickCallback,
) => {
  ////////////////////////////////////////////////////////////////////////////////HOOKS///////////////////////////////////////////////////////////////////////////////////////
  const handleClick = useCallback(
    (event: EventTarget) => {
      try {
        if (!ref.current || ref.current.contains(event.target as Node)) {
          return;
        }
        callback(event);
      } catch (error) {}
    },
    [ref, callback],
  );

  useEffect(() => {
    const listener = (event: EventTarget) => handleClick(event);

    try {
      document.addEventListener("mousedown", listener as EventListener);
      document.addEventListener("touchstart", listener as EventListener);

      return () => {
        document.removeEventListener("mousedown", listener as EventListener);
        document.removeEventListener("touchstart", listener as EventListener);
      };
    } catch (error) {
      console.error("Erreur lors de l'ajout des event listeners:", error);
      return () => {};
    }
  }, [handleClick]);
  ////////////////////////////////////////////////////////////////////////////////HOOKS///////////////////////////////////////////////////////////////////////////////////////
};
////////////////////////////////////////////////////////////////////////////////FUNCTIONS///////////////////////////////////////////////////////////////////////////////////////
