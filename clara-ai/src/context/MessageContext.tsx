////////////////////////////////////////////////////////////////////////////////IMPORTS///////////////////////////////////////////////////////////////////////////////////////
"use client";

import React, {
  createContext,
  useContext,
  useCallback,
  useState,
  useEffect,
  useMemo,
  memo,
} from "react";
import { api } from "~/trpc/react";
////////////////////////////////////////////////////////////////////////////////IMPORTS///////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////TYPES///////////////////////////////////////////////////////////////////////////////////////
type MessageContextType = {
  refreshDailyMessages: () => void;
};
////////////////////////////////////////////////////////////////////////////////TYPES///////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////FUNCTIONS///////////////////////////////////////////////////////////////////////////////////////
// Créez le contexte avec une valeur par défaut
const MessageContext = createContext<MessageContextType | null>(null);

const MessageProvider = memo(function MessageProvider({
  children,
}: {
  children: React.ReactNode;
}) {
  ///////////////////////////////////////////////////////////////////////////////HOOKS///////////////////////////////////////////////////////////////////////////////////////
  const [isReady, setIsReady] = useState(false);

  // Utilisez l'API tRPC pour rafraîchir les messages quotidiens
  const { refetch: refetchDailyMessages } =
    api.user.getDailyMessages.useQuery();

  // Fonction de rafraîchissement
  const refreshDailyMessages = useCallback(() => {
    refetchDailyMessages();
  }, [refetchDailyMessages]);

  // Marquer comme prêt après l'hydratation
  useEffect(() => {
    setIsReady(true);
  }, []);

  const value = useMemo<MessageContextType>(
    () => ({
      refreshDailyMessages,
    }),
    [refreshDailyMessages],
  );
  ///////////////////////////////////////////////////////////////////////////////HOOKS///////////////////////////////////////////////////////////////////////////////////////

  ///////////////////////////////////////////////////////////////////////////////RENDER///////////////////////////////////////////////////////////////////////////////////////
  // Ne rendre que si prêt
  if (!isReady) {
    return null;
  }

  return (
    <MessageContext.Provider value={value}>{children}</MessageContext.Provider>
  );
});

// Hook pour utiliser le contexte
export const useMessageContext = () => {
  const context = useContext(MessageContext);
  if (context === null) {
    // Retourner une fonction vide au lieu de throw une erreur
    return () => {};
  }
  return context.refreshDailyMessages;
};

export { MessageProvider };
