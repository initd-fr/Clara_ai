//! Bonne pratique pour les couleurs avec daisyui

// Couleurs de base :

// base-100 : Couleur de fond principale (background) pour vos pages
// base-200 : Couleur de fond secondaire, légèrement contrastée avec base-100 (pour les cards, sections...)
// base-300 : Couleur tertiaire, encore plus contrastée (pour les éléments surélevés)
// base-content : Couleur de texte principale qui contraste bien avec base-100

// Couleurs sémantiques :

// primary : Couleur principale de votre marque, utilisée pour les actions principales (CTA buttons, liens importants)
// primary-content : Couleur du texte qui va sur les éléments primary
// secondary : Couleur secondaire pour les actions moins importantes
// accent : Couleur d'accent pour mettre en valeur certains éléments

// États et feedback :

// info : Pour les messages d'information/notifications
// success : Pour les messages de succès/validations
// warning : Pour les avertissements
// error : Pour les erreurs

// Éléments neutres :

// neutral : Couleur neutre pour les éléments moins importants
// neutral-content : Texte sur fond neutral
